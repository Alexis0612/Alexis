    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        //validaciones
        if(txtIdProductos.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el Id del producto");
            txtIdProductos.grabFocus();
            return;
        }
        ConectarProductos con = new ConectarProductos();
        Producto producto = con.getProducto(txtIdProductos.getText());
        if(producto != null)
        {
            JOptionPane.showMessageDialog(rootPane,"Usuario ya existe");
            txtIdProductos.setText("");
            txtIdProductos.grabFocus();
            con.cerrarConexion();
            return;
        }
        if(!validarCampos())
        {
            con.cerrarConexion();
            return;
        }
        producto = new Producto(txtIdProductos.getText(),
            txtDescripcion.getText(),
            Integer.parseInt(txtPrecio.getText()),  
            cmbIVA.getSelectedIndex(),
            txtNota.getText());

        if(con.insertarProducto(producto))
        {
            JOptionPane.showMessageDialog(rootPane,"Producto ingresado correctamente");
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Producto no ingresado correctamente");
        }

        //limpiar los campos
        limpiar();
        con.cerrarConexion();
        llenarTabla();
    }//GEN-LAST:event_btnNuevoActionPerformed

    //metodo privado para validar campos
    private boolean validarCampos()
    {
        if(txtDescripcion.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el nombre del producto","Descripcion", WIDTH);
            txtDescripcion.grabFocus();
            return false;
        }
        
        if(txtPrecio.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el precio del producto","Precio", WIDTH);
            txtPrecio.grabFocus();
            return false;
        }
        
        //revisar que no se ingrese un valor distinto a un numero
        if (!Utilerias.esNumero(txtPrecio.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor numerico", "Precio", WIDTH);
            txtPrecio.setText("");
            txtPrecio.grabFocus();
            return false;
        }

        //revisar que el valor no sea negativo
        int precio = Integer.parseInt(txtPrecio.getText());
        if (precio <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Debe digitar un valor mayor a cero", "Precio", WIDTH);
            txtPrecio.setText("");
            txtPrecio.grabFocus();
            return false;
        }
        
        if(cmbIVA.getSelectedIndex() == 0)
        {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar el IVA","IVA", WIDTH);
            cmbIVA.grabFocus();
            return false;
        }
        
        if (txtNota.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar una Nota del producto", "Nota", WIDTH);
            txtNota.grabFocus();
            return false;
        }        
        return true;
    }
    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
        //crear un archivo PDF con la informacion de la tabla usuario
        Document documento = new Document();
        try {
            PdfWriter.getInstance(documento, new FileOutputStream("Productos.pdf"));
            String texto = "Reporte de Productos";
            Paragraph parrafo = new Paragraph(texto);
            documento.open();
            documento.add(parrafo);
            ConectarProductos conectar = new ConectarProductos();
            ResultSet rs = conectar.getProductos();

            while(rs.next())
            {
                texto = "ID: " + rs.getString("Id_productos") + " - Descripcion: "
                + rs.getString("Descripcion") + " - Precio: "
                + rs.getString("Precio") + " - IVA: "
                + IVA(rs.getString("IVA") + " - Nota: "
                + rs.getString("Nota"));

                parrafo = new Paragraph(texto);
                documento.add(parrafo);
            }
            conectar.cerrarConexion();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(frmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(frmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        finally
        {
            documento.close();
            JOptionPane.showMessageDialog(rootPane, "Reporte generado");
        }
    }//GEN-LAST:event_btnPDFActionPerformed

