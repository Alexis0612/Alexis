    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int resp = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de eliminar el producto");
        if(resp != 0)
        {
            return;
        }
        //validaciones
        if(txtIdProductos.getText().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar el Id del producto");
            txtIdProductos.grabFocus();
            return;
        }
        ConectarProductos con = new ConectarProductos();
        Producto producto = con.getProducto(txtIdProductos.getText());
        if(producto == null)
        {
            JOptionPane.showMessageDialog(rootPane,"Producto no existe");
            txtIdProductos.setText("");
            txtIdProductos.grabFocus();
            con.cerrarConexion();
            return;
        }
        if(con.deleteProducto(producto))
        {
            JOptionPane.showMessageDialog(rootPane, "Producto eliminado correctamente");
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Producto no eliminado");
        }
        limpiar();
        con.cerrarConexion();
        llenarTabla();
    }//GEN-LAST:event_btnEliminarActionPerformed
    
    //crear un metodo privado para mostrar los registros en la tabla
    private void llenarTabla()
    {
        //generamos una instancia a la clase conectar
        ConectarProductos conectar = new ConectarProductos();
        
        try {
            //creamos un arreglo para los titulos de la tabla
            String titulos[] = {"Id Producto", "Descripcion", "Precio", "IVA", "Nota"};
            miTabla = new DefaultTableModel(null, titulos);
            String registros[] = new String[5];
        
            ResultSet rs = conectar.getProductos();
            while(rs.next())
            {
                registros[0] = rs.getString("Id_productos");
                registros[1] = rs.getString("Descripcion");
                registros[2] = rs.getString("Precio");
                registros[3] = IVA(rs.getString("IVA")); 
                registros[4] = rs.getString("Nota");                 
                miTabla.addRow(registros);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmProductos.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblTabla.setModel(miTabla);
        conectar.cerrarConexion();
    }

    //metodo para definir el perfil
    private String IVA(String IdIVA)
    {
        switch(IdIVA)
        {
            case "0": return "0%";
            case "1": return "5%";
            case "2": return "12%";
            default: return "No es una seleccion valida";
        }
    }
